            La rééducation pour rendre sa mobilité à l’animal
        </br></br>

        Une maladie, un accident ou la vieillesse sont souvent synonymes de motricité diminuée. Les répercussions sur la vie de l’animal sont souvent synonymes de déclin. 
            </br>
            En effet, une affection non traitée peut entraîner des complications : en s’efforçant de mobiliser d’autres muscles de manière non naturelle afin de limiter sa douleur, l’animal crée par compensation de nouveaux troubles musculo-squelettiques.
            </br></br>

            80 % des chiens ont besoin de rééducation après une intervention
            </br></br>

